import { NextFunction, Request, Response } from "express";

import Controller from "@src/controllers/Controller";

import BoardService from "@src/services/BoardService";

import resTypes from "@src/utils/resTypes";
import Board from "@src/models/BoardModel";

class CreateController extends Controller {
    private result: Board | string;
    constructor() {
        super();
        this.result = "";
    }

    async doService(
        req: Request,
        res: Response,
        next: NextFunction
    ): Promise<void> {
        try {
            this.result = await BoardService.create(req);
        } catch (e: unknown) {
            this.result = "InternalServerError";
            console.log(e);
        }
    }

    async doResolve(
        req: Request,
        res: Response,
        next: NextFunction
    ): Promise<void> {
        switch (this.result) {
            case "BadRequest":
                resTypes.badRequestErrorRes(res);
                break;
            case "InternalServerError":
                resTypes.internalErrorRes(res);
                break;
            case "CannotFindItem":
                resTypes.cannotFindItemRes(res, "user");
                break;
            default:
                resTypes.successRes(res, "게시판 생성");
        }
    }
}

export default CreateController;
